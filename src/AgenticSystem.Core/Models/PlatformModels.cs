// #42 — Dependency Graph
// ═══════════════════════════════════════════════════════════

/// <summary>
/// System dependency node for impact analysis.
/// </summary>
public class DependencyNode
{
    public string Id { get; init; } = string.Empty;
    public string Name { get; init; } = string.Empty;
    public DependencyType Type { get; init; }
    public DependencyHealth Health { get; set; } = DependencyHealth.Healthy;
    public List<string> DependsOn { get; init; } = [];
    public List<string> DependedBy { get; init; } = [];
    public Dictionary<string, string> Metadata { get; init; } = new();
}

public enum DependencyType { Agent, Tool, Service, Database, ExternalApi, McpServer, Model }
public enum DependencyHealth { Healthy, Degraded, Unhealthy, Unknown }

/// <summary>
/// Impact analysis result for a dependency change.
/// </summary>
public class ImpactAnalysis
{
    public string ChangedNodeId { get; init; } = string.Empty;
    public List<string> DirectlyAffected { get; init; } = [];
    public List<string> IndirectlyAffected { get; init; } = [];
    public int TotalImpactedNodes { get; init; }
    public double SeverityScore { get; init; }
}

