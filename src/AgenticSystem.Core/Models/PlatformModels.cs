// #49 — Agent Communication Protocol
// ═══════════════════════════════════════════════════════════

/// <summary>
/// A formal message in the inter-agent communication protocol.
/// </summary>
public class AgentMessage
{
    public string MessageId { get; init; } = Guid.NewGuid().ToString("N");
    public string SenderId { get; init; } = string.Empty;
    public string ReceiverId { get; init; } = string.Empty;
    public AgentMessageType MessageType { get; init; }
    public string Content { get; init; } = string.Empty;
    public Dictionary<string, object> Payload { get; init; } = new();
    public string? ConversationId { get; init; }
    public string? InReplyTo { get; init; }
    public AgentMessagePriority Priority { get; init; } = AgentMessagePriority.Normal;
    public DateTime Timestamp { get; init; } = DateTime.UtcNow;
    public DateTime? ExpiresAt { get; init; }
}

public enum AgentMessageType
{
    Request,
    Response,
    Delegation,
    Notification,
    Handoff,
    StatusUpdate,
    ErrorReport,
    CapabilityQuery
}

public enum AgentMessagePriority { Low, Normal, High, Urgent }
